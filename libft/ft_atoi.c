#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	int				signal;
	int				output;

	i = 0;
	signal = 1;
	output = 0;
	while (str[i] && (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signal = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		output = (output * 10) + (str[i] - '0');
		i++;
	}
	return (signal * output);
}
